
package sreda1;


class Square extends Rectangle{

    public Square(double a, double b,String name){
        super(a, b, name);
        
    }

    @Override
    public String toString() {
        return super.toString(); 
    }

    @Override
    public double area() {
        return super.area();
    }

    @Override
    public double circumference() {
        return super.circumference(); 

   
}}

  
  
    

